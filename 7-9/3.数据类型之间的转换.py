# 整数，小数，字符串之间的转换
# str()函数用于将一个变量转化成字符串类型的数据
a=10
print(type(a))
a1=str(a)
print('a转换之后的类型；',type(a1))

b=9.88
print(type(b))
b1=str(b)
print('b转换之后的类型：',type(b1))

c=2.33
print(type(c))
c1=int(c)
print('c转换之后的类型：',type(c1))

d='20'
print(type(d))
d1=int(d)
print('d转化之后的类型',type(d1))

# int():将一个纯整数类型的字符串转化为整数类型的数据
f='20'
print(type(f))
f1=int(f)
print('f转化之后的类型',type(f1))

# float()函数：将一个纯数字类型的字符串转化成自小数类型的数据
q='99.80'
print(type(q))
q1=float(q)
print('q转化之后的类型',type(q1))

